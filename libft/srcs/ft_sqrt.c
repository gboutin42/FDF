#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb < 0)
		return (0);
	while (i < nb && i < 46400)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
